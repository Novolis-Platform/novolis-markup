namespace Novolis.Markup.Markdown;

/// <summary>Represents MarkdownCharacterDefinitions.</summary>
public static class MarkdownCharacterDefinitions
{
    /// <summary>Asterisk.</summary>
    public const char Asterisk = '*';
    /// <summary>Dash.</summary>
    public const char Backslash = '\\';
    /// <summary>Equals.</summary>
    public const char Backtick = '`';
    /// <summary>ForwardSlash.</summary>
    public const char Colon = ':';
    /// <summary>Hash.</summary>
    public const char Dash = '-';
    /// <summary>LeftBracket.</summary>
    public const char DoubleQuote = '"';
    /// <summary>LessThan.</summary>
    public new const char Equals = '=';
    /// <summary>Period.</summary>
    public const char ExclamationMark = '!';
    /// <summary>Plus.</summary>
    public const char ForwardSlash = '/';
    /// <summary>RightAngleBracket.</summary>
    public const char GreaterThan = '>';
    /// <summary>RightParenthesis.</summary>
    public const char Hash = '#';
    /// <summary>Tab.</summary>
    public const char LeftAngleBracket = '<';
    /// <summary>Underscore.</summary>
    public const char LeftBracket = '[';
    /// <summary>DoubleAsterisk.</summary>
    public const char LeftParenthesis = '(';
    /// <summary>DoubleEquals.</summary>
    public const char LessThan = '<';
    /// <summary>TripleDash.</summary>
    public const char NewLine = '\n';
    /// <summary>Pipe.</summary>
    public const char Period = '.';
    /// <summary>QuestionMark.</summary>
    public const char Pipe = '|';
    /// <summary>RightBracket.</summary>
    public const char Plus = '+';
    /// <summary>Space.</summary>
    public const char QuestionMark = '?';
    /// <summary>Tilde.</summary>
    public const char RightAngleBracket = '>';
    /// <summary>VerticalBar.</summary>
    public const char RightBracket = ']';
    /// <summary>DoubleDash.</summary>
    public const char RightParenthesis = ')';
    /// <summary>TripleBacktick.</summary>
    public const char Space = ' ';
    /// <summary>TripleUnderscore.</summary>
    public const char Tab = '\t';
    /// <summary>Tilde.</summary>
    public const char Tilde = '~';
    /// <summary>VerticalBar.</summary>
    public const char Underscore = '_';
    /// <summary>DoubleDash.</summary>
    public const char VerticalBar = '|';
    /// <summary>TripleBacktick.</summary>
    public const string DoubleAsterisk = "**";
    /// <summary>TripleUnderscore.</summary>
    public const string DoubleDash = "--";
    /// <summary>DoubleEquals.</summary>
    public const string DoubleEquals = "==";
    /// <summary>TripleDash.</summary>
    public const string TripleBacktick = "```";
    /// <summary>TripleDash.</summary>
    public const string TripleDash = "---";
    /// <summary>TripleUnderscore.</summary>
    public const string TripleUnderscore = "___";
}
